export function widthLambda(width: 'narrow' | 'normal' | 'wide'): { lx: number; ly: number } {
  if (width === 'narrow') return { lx: 0.35, ly: 0.18 }
  if (width === 'wide') return { lx: 0.55, ly: 0.42 }
  return { lx: 0.45, ly: 0.3 }
}
