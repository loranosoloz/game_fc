export function crestKeyForShortName(shortName: string): string | undefined {
  return BY_SHORT[shortName.toUpperCase()]
}

export function crestUrl(crestKey: string | null | undefined): string | null {
  if (!crestKey) return null
  return `/crests/${crestKey}.png`
}

export function crestUrlForClub(club: {
  crestKey?: string | null
  shortName: string
}): string | null {
  const key = club.crestKey ?? crestKeyForShortName(club.shortName)
  return crestUrl(key)
}
